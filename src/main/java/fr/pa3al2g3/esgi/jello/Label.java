package fr.pa3al2g3.esgi.jello;

public class Label {

    private final String title;
    private final String color;

    public Label(String title, String color) {
        this.title = title;
        this.color = color;
    }

    public String getTitle() {
        return title;
    }

    public String getColor() {
        return color;
    }
}
